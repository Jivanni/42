void	ft_rev_int_tab(int *tab, int size)
{
	int x;
	int y;
	int temp;

	x = 0;
	y = size - 1;
	while (x < y)
	{
		temp = *(tab + x);
		*(tab + x) = *(tab + y);
		*(tab + y) = temp;
		x++;
		y--;
	}
}
